package InterfazCoche;

public class Coche {

    String fabricante;
    int year;
    int centimetrosCubicos;

    public Coche() {}

    public Coche(String fabricante, int year, int centimetrosCubicos) {
        this.fabricante = fabricante;
        this.year = year;
        this.centimetrosCubicos = centimetrosCubicos;
    }
}

